package com.javat.string;

public class IsEmpty
     {
	    public static void main(String args[]) 
	    { 
	        // non-empty string 
	        String str1 = "Hello_Gfg"; 
	  
	        // empty string 
	        String str2 = ""; 
	   
	        System.out.println(str1.isEmpty()); 
	  
	        System.out.println(str2.isEmpty()); 
	    } 
	} 
